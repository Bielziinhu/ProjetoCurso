    public void listarCurso() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        //Reseta a tabela para receber os dados
        model.setRowCount(0);

        //busca todos os cursos e joga em uma list
        List<Curso> cadastros = cursoRepository.findAll();
        
        //Adiciona em cada coluna do JTable
        for (Curso curso : cadastros) {
            model.addRow(new Object[]{
                curso.getNome(),
                curso.getCodigoCurso(),
                curso.isAtivo()
            });
        }
    }
    
    public void listarPesquisa(String pesquisar){
    
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        List<Curso> listaPesquisa = cursoRepository.findByNome(pesquisar);

        for (Curso curso : listaPesquisa) {
            model.addRow(new Object[]{
                curso.getNome(),
                curso.getCodigoCurso(),
                curso.isAtivo()
           });
  }
    }

