    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        String idStr = JOptionPane.showInputDialog("Digite o ID do curso a ser excluído:");

        try {
            long id = Integer.parseInt(idStr);
            cursoRepository.deleteById(id);

            JOptionPane.showMessageDialog(null, "Curso excluído com sucesso!");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Id não encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir o curso: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

